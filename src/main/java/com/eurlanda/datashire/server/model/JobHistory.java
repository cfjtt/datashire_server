package com.eurlanda.datashire.server.model;

import java.util.Date;

public class JobHistory {
    private String taskId;

    private Integer repositoryId;

    private Integer SquidFlowId;

    private Integer jobId;

    private Integer status;

    private String debugSquids;

    private String destinationSquids;

    private String caller;

    private Date createTime;

    private Date updateTime;

    public String getTaskId() {
        return taskId;
    }

    public void setTaskId(String taskId) {
        this.taskId = taskId;
    }

    public Integer getRepositoryId() {
        return repositoryId;
    }

    public void setRepositoryId(Integer repositoryId) {
        this.repositoryId = repositoryId;
    }

    public Integer getSquidFlowId() {
        return SquidFlowId;
    }

    public void setSquidFlowId(Integer SquidFlowId) {
        this.SquidFlowId = SquidFlowId;
    }

    public Integer getJobId() {
        return jobId;
    }

    public void setJobId(Integer jobId) {
        this.jobId = jobId;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getDebugSquids() {
        return debugSquids;
    }

    public void setDebugSquids(String debugSquids) {
        this.debugSquids = debugSquids;
    }

    public String getDestinationSquids() {
        return destinationSquids;
    }

    public void setDestinationSquids(String destinationSquids) {
        this.destinationSquids = destinationSquids;
    }

    public String getCaller() {
        return caller;
    }

    public void setCaller(String caller) {
        this.caller = caller;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }
}