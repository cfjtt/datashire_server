package com.eurlanda.datashire.entity.operation;

import com.eurlanda.datashire.entity.Column;
import com.eurlanda.datashire.entity.ReferenceColumn;
import com.eurlanda.datashire.entity.Transformation;
import com.eurlanda.datashire.entity.TransformationLink;

import java.util.List;

public class DataSquidCollectionProperty {
	
	public DataSquidCollectionProperty(){
	}
	
	public DataSquidCollectionProperty(int squidId, List<Column> columns,
			List<ReferenceColumn> referenceColumns,
			List<Transformation> transformations,
			List<TransformationLink> transformLinks) {
		super();
		this.squidId = squidId;
		this.columns = columns;
		this.referenceColumns = referenceColumns;
		this.transformations = transformations;
		this.transformLinks = transformLinks;
	}

	public int squidId;
	public List<Column> columns;
	public List<ReferenceColumn> referenceColumns;
	public List<Transformation> transformations;
	public List<TransformationLink> transformLinks;
	public int getSquidId() {
		return squidId;
	}

	public void setSquidId(int squidId) {
		this.squidId = squidId;
	}

	public List<Column> getColumns() {
		return columns;
	}

	public void setColumns(List<Column> columns) {
		this.columns = columns;
	}

	public List<ReferenceColumn> getReferenceColumns() {
		return referenceColumns;
	}

	public void setReferenceColumns(List<ReferenceColumn> referenceColumns) {
		this.referenceColumns = referenceColumns;
	}

	public List<Transformation> getTransformations() {
		return transformations;
	}

	public void setTransformations(List<Transformation> transformations) {
		this.transformations = transformations;
	}

	public List<TransformationLink> getTransformLinks() {
		return transformLinks;
	}

	public void setTransformLinks(List<TransformationLink> transformLinks) {
		this.transformLinks = transformLinks;
	}
}
