package com.eurlanda.datashire.entity.operation;

import java.util.Map;

/**
 * webServiceFunction definition
 * 2014-11-27
 * @author Akachi
 * @E-Mail zsts@hotmail.com
 */
public class WebServiceFunction {
	private String functionName;
	private Map<String,String> parameter;
	private String returnType;
	private String functionUrl;
	private String requestXml;
	private String xmlns_tns;
	private String type;
	private Map<String,String> returnTypes;
	private String method;
	
	
	
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public Map<String, String> getReturnTypes() {
		return returnTypes;
	}
	public void setReturnTypes(Map<String, String> returnTypes) {
		this.returnTypes = returnTypes;
	}
	public String getFunctionName() {
		return functionName;
	}
	public void setFunctionName(String functionName) {
		this.functionName = functionName;
	}
	public Map<String, String> getParameter() {
		return parameter;
	}
	public void setParameter(Map<String, String> parameter) {
		this.parameter = parameter;
	}
	public String getReturnType() {
		return returnType;
	}
	public void setReturnType(String returnType) {
		this.returnType = returnType;
	}
	public String getFunctionUrl() {
		return functionUrl;
	}
	public void setFunctionUrl(String functionUrl) {
		this.functionUrl = functionUrl;
	}
	public String getRequestXml() {
		return requestXml;
	}
	public void setRequestXml(String requestXml) {
		this.requestXml = requestXml;
	}
	public String getXmlns_tns() {
		return xmlns_tns;
	}
	public void setXmlns_tns(String xmlns_tns) {
		this.xmlns_tns = xmlns_tns;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
}
