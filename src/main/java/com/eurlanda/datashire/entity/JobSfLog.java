package com.eurlanda.datashire.entity;

import com.eurlanda.datashire.annotation.ColumnMpping;
import com.eurlanda.datashire.annotation.MultitableMapping;

import java.sql.Types;

@MultitableMapping(pk = "", name = "DS_SYS_SF_LOG", desc = "")
public class JobSfLog {
	@ColumnMpping(name = "id", desc = "", nullable = false, precision = 0, type = Types.INTEGER, valueReg = "")
	private String id;
	@ColumnMpping(name = "task_id", desc = "", nullable = false, precision = 36, type = Types.VARCHAR, valueReg = "")
	private String task_id;
	@ColumnMpping(name = "message", desc = "", nullable = true, precision = 500, type = Types.VARCHAR, valueReg = "")
	private String message;
	@ColumnMpping(name = "create_time", desc = "", nullable = true, precision = 0, type = Types.TIMESTAMP, valueReg = "")
	private String create_time;
	@ColumnMpping(name = "log_level", desc = "", nullable = true, precision = 0, type = Types.INTEGER, valueReg = "")
	private String log_level;
	@ColumnMpping(name = "squid_id", desc = "", nullable = true, precision = 0, type = Types.INTEGER, valueReg = "")
	private String squid_id;
	@ColumnMpping(name = "tsquid_id", desc = "", nullable = true, precision = 40, type = Types.VARCHAR, valueReg = "")
	private String tsquid_id;
	@ColumnMpping(name = "tsquid_type", desc = "", nullable = true, precision = 0, type = Types.INTEGER, valueReg = "")
	private String tsquid_type;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTask_id() {
		return task_id;
	}
	public void setTask_id(String task_id) {
		this.task_id = task_id;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getLog_level() {
		return log_level;
	}
	public void setLog_level(String log_level) {
		this.log_level = log_level;
	}
	public String getSquid_id() {
		return squid_id;
	}
	public void setSquid_id(String squid_id) {
		this.squid_id = squid_id;
	}
	public String getTsquid_id() {
		return tsquid_id;
	}
	public void setTsquid_id(String tsquid_id) {
		this.tsquid_id = tsquid_id;
	}
	public String getTsquid_type() {
		return tsquid_type;
	}
	public void setTsquid_type(String tsquid_type) {
		this.tsquid_type = tsquid_type;
	}
	

}
