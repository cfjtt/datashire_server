package com.eurlanda.datashire.entity;

import com.eurlanda.datashire.annotation.ColumnMpping;
import com.eurlanda.datashire.annotation.MultitableMapping;

import java.sql.Types;

@MultitableMapping(pk="id", name = "DS_SYS_REPORT_FOLDER_MAPPING", desc = "")
public class ReportFolderMapping {
	// 节点ID
	@ColumnMpping(name="id", desc="", nullable=false, precision=0, type=Types.INTEGER, valueReg="")
	private Integer id;
	// 节点ID REPOSITORY_ID
	@ColumnMpping(name="repository_id", desc="", nullable=false, precision=0, type=Types.INTEGER, valueReg="")
	private Integer repository_id;
	// 节点ID SQUID_ID
	@ColumnMpping(name="squid_id", desc="", nullable=true, precision=0, type=Types.INTEGER, valueReg="")
	private Integer squid_id;
	// 文件名称 SQUID_PUBLISHING_NAME
	@ColumnMpping(name="report_name", desc="", nullable=true, precision=100, type=Types.VARCHAR, valueReg="")
	private String report_name;
	// 创建日期
	@ColumnMpping(name="creation_date", desc="", nullable=true, precision=0, type=Types.TIMESTAMP, valueReg="")
	private String creation_date;
	// 父节点ID
	@ColumnMpping(name="folder_id", desc="", nullable=true, precision=0, type=Types.INTEGER, valueReg="")
	private Integer folder_id;
	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}
	/**
	 * @return the repository_id
	 */
	public Integer getRepository_id() {
		return repository_id;
	}
	/**
	 * @param repository_id the repository_id to set
	 */
	public void setRepository_id(Integer repository_id) {
		this.repository_id = repository_id;
	}
	/**
	 * @return the squid_id
	 */
	public Integer getSquid_id() {
		return squid_id;
	}
	/**
	 * @param squid_id the squid_id to set
	 */
	public void setSquid_id(Integer squid_id) {
		this.squid_id = squid_id;
	}
	/**
	 * @return the creation_date
	 */
	public String getCreation_date() {
		return creation_date;
	}
	/**
	 * @param creation_date the creation_date to set
	 */
	public void setCreation_date(String creation_date) {
		this.creation_date = creation_date;
	}
	/**
	 * @return the folder_id
	 */
	public Integer getFolder_id() {
		return folder_id;
	}
	/**
	 * @param folder_id the folder_id to set
	 */
	public void setFolder_id(Integer folder_id) {
		this.folder_id = folder_id;
	}
	/**
	 * @return the squid_publishing_name
	 */
	/**
	 * @return the report_name
	 */
	public String getReport_name() {
		return report_name;
	}
	/**
	 * @param report_name the report_name to set
	 */
	public void setReport_name(String report_name) {
		this.report_name = report_name;
	}
}
