package com.eurlanda.datashire.squidVersion;

import com.eurlanda.datashire.entity.DSBaseModel;

/**
 * 历史版本回调信息类
 * Created by Administrator on 2016/9/23.
 */
public class SquidFlowHistoryCallBackInfo extends DSBaseModel {

    private int squidFlowId;
    private String squidFlowName;
    private int callBackStepInfo;
    private int stepCount;

    public SquidFlowHistoryCallBackInfo() {
    }

    public SquidFlowHistoryCallBackInfo(int squidFlowId, String squidFlowName, int callBackStepInfo, int stepCount) {
        this.squidFlowId = squidFlowId;
        this.squidFlowName = squidFlowName;
        this.callBackStepInfo = callBackStepInfo;
        this.stepCount = stepCount;
    }

    public int getSquidFlowId() {
        return squidFlowId;
    }

    public void setSquidFlowId(int squidFlowId) {
        this.squidFlowId = squidFlowId;
    }

    public String getSquidFlowName() {
        return squidFlowName;
    }

    public void setSquidFlowName(String squidFlowName) {
        this.squidFlowName = squidFlowName;
    }

    public int getCallBackStepInfo() {
        return callBackStepInfo;
    }

    public void setCallBackStepInfo(int callBackStepInfo) {
        this.callBackStepInfo = callBackStepInfo;
    }

    public int getStepCount() {
        return stepCount;
    }

    public void setStepCount(int stepCount) {
        this.stepCount = stepCount;
    }

    @Override
    public String toString() {
        return "SquidFlowHistoryCallBackInfo{" +
                "squidFlowId=" + squidFlowId +
                ", squidFlowName='" + squidFlowName + '\'' +
                ", callBackStepInfo=" + callBackStepInfo +
                ", stepCount=" + stepCount +
                '}';
    }
}
