package com.eurlanda.datashire.exception;

import java.sql.SQLException;

/**
 * 自定义异常类 (导入版本不匹配)
 * @author yi.zhou
 * @date   2015-01-23
 */
public class ImportVersionException extends SQLException {
	
	private static final long serialVersionUID = 1L;

	public ImportVersionException() {
        super();
    }
    
    public ImportVersionException(String msg) {
        super(msg);
    }
    
    public ImportVersionException(String msg, Throwable cause) {
        super(msg, cause);
    }
    
    public ImportVersionException(Throwable cause) {
        super(cause);
    }
}
