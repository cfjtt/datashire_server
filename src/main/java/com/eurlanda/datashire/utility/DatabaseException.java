package com.eurlanda.datashire.utility;

import java.text.MessageFormat;

public class DatabaseException extends Exception {

	private static final long serialVersionUID = 1300141194617241844L;

	public DatabaseException() {
		super();
	}
	
	public DatabaseException(String message) {
		super(message);
	}

	public DatabaseException(Throwable cause) {
		super(cause);
	}
	
	public DatabaseException(String message, Throwable cause) {
		super(message, cause);
	}
	
    public DatabaseException(String pattern, Object... arguments) {
        super(MessageFormat.format(pattern, arguments));
    }
 
    public DatabaseException(Throwable cause, String pattern, Object... arguments) {
        super(MessageFormat.format(pattern, arguments), cause);
    }
    
}
