package com.eurlanda.datashire.utility;

public class EncodingUtils {
	/**
	 * 根据枚举值转换为string
	 * @return
	 */
   public static String getEncoding(int enumValue)
   {
	   String encoding="";
	   //UTF-8, ASCII, GBK, UNICODE, GB2312, BIG5
	   if(0==enumValue)
	   {
		   encoding="UTF-8";
	   }else if(1==enumValue)
	   {
		   encoding="ASCII";
	   }else if(2==enumValue)
	   {
		   encoding="GBK";
	   }else if(3==enumValue)
	   {
		   encoding="UNICODE";
	   }else if(4==enumValue)
	   {
		   encoding="GB2312";
	   }else if(5==enumValue)
	   {
		   encoding="BIG5";
	   }else
	   {
		   encoding="GBK";
	   }
	   return encoding;
   }
}
